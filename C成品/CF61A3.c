#include <stdio.h>// 函数头 

int main()//主函数入口 
{
	char a[100]={'\0'}, b[100], c[100] = {'\0'};//定义三个字符数组 
	int i;//定义一个变量 
	scanf("%s %s",a,b);//读入两个字符串 
	for (i = 0; i < 100; i++) {//历遍数组 
		if (a[i] != '\0') {//如果不为最后一个 
			if (a[i] == b[i]) {//如果相同 
				c[i] = '0';//先新数组存入0 
			}else{//反之 
				c[i] = '1';//先新数组存入1 				
			}
		}
	}
	for (i = 0; i < 100 && c[i] != '\0'; i++) {//历C遍数组
		printf("%c", c[i]);//输出 
	}

	return 0;//主函数结束 
}
